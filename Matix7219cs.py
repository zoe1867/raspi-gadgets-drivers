class Matrix7219:

    def __init__(self):
        self.Matrix7219_CLK = 2   
        self.Matrix7219_CS = 3  
        self.Matrix7219_DIN = 4  

    def write_byte(self, dat):
        GPIO.output(self.Matrix7219_CS, GPIO.LOW)
        count = 0
        while (count<8):
            GPIO.output(self.Matrix7219_CLK, GPIO.LOW)
            tmp = dat&0x80
            if tmp == 0x80:
                GPIO.output(self.Matrix7219_DIN, GPIO.HIGH)
            else:
                GPIO.output(self.Matrix7219_DIN, GPIO.LOW)
            GPIO.output(self.Matrix7219_CLK, GPIO.HIGH)
            dat = dat<<1   
            count = count + 1    

    def write_data(self, addr, wdat):
        GPIO.output(self.Matrix7219_CS, GPIO.LOW)
        self.write_byte(addr)
        self.write_byte(wdat)
        GPIO.output(self.Matrix7219_CS, GPIO.HIGH)     

    def init(self):

        GPIO.setmode(GPIO.BCM)  
        time.sleep(1)  
        GPIO.setup(self.Matrix7219_CLK, GPIO.OUT)  
        GPIO.setup(self.Matrix7219_CS, GPIO.OUT) 
        GPIO.setup(self.Matrix7219_DIN, GPIO.OUT) 

        self.write_data(0x09,0x00)
        self.write_data(0x0a,0x03)
        self.write_data(0x0b,0x07)
        self.write_data(0x0c,0x01)
        self.write_data(0x0f,0x00)

    def display_char(self, chr):
        for i in range(8):
            tmp = 0
            if chr >='0' and chr <='9':
                tmp = ord(chr) - ord('0')
            elif chr >='a' and chr <='z':
                tmp = ord(chr) - ord('a') + 10
            elif chr >='A' and chr <='Z':
                tmp = ord(chr) - ord('A') + 10
            else:
                tmp = 0
            self.write_data(i+1, displib[tmp][i])

        

