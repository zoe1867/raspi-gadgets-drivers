Max7219_CLK = 2   
Max7219_CS = 3  
Max7219_DIN = 4  
  
GPIO.setmode(GPIO.BCM)  
time.sleep(1)  
  
GPIO.setup(Max7219_CLK, GPIO.OUT)  
GPIO.setup(Max7219_CS, GPIO.OUT) 
GPIO.setup(Max7219_DIN, GPIO.OUT) 

def write_max7219_byte(dat):
    GPIO.output(Max7219_CS, GPIO.LOW)
    count = 0
    while (count<8):
        GPIO.output(Max7219_CLK, GPIO.LOW)
        tmp = dat&0x80
        if tmp == 0x80:
            GPIO.output(Max7219_DIN, GPIO.HIGH)
        else:
            GPIO.output(Max7219_DIN, GPIO.LOW)
        GPIO.output(Max7219_CLK, GPIO.HIGH)
        dat = dat<<1   
        count = count + 1    

def write_max7219(addr, wdat):
    GPIO.output(Max7219_CS, GPIO.LOW)
    write_max7219_byte(addr)
    write_max7219_byte(wdat)
    GPIO.output(Max7219_CS, GPIO.HIGH)     

def max7219_init():
    write_max7219(0x09,0x00)
    write_max7219(0x0a,0x03)
    write_max7219(0x0b,0x07)
    write_max7219(0x0c,0x01)
    write_max7219(0x0f,0x00)

